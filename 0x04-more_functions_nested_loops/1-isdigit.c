/**
*_isdigit - This function checks if the character c is a digit between 0 - 9.
*@c: The character to be checked.
*Return: This function returns 1 if  c is a digit otherwise 0
*/

int _isdigit(int c)
{
	if (c >= '0' && c <= '9')
		{
		return (1);
				}
	else
		{
		return (0);

}
return (0);
}
