/**
*_isupper - This function checks if c is an uppercase character.
*@c: The character to be checked.
*Return: If c is uppercase the function returns 1 otherwise 0
*/


int _isupper(int c)
{
	if ((c >= 'A' && c <= 'Z'))
		{
		return (1);
				}
	else
		{
		return (0);


}
return (0);
}
